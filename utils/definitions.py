DEFINITIONS_VERSION = "v1"

DEFINITIONS_MD = f"""
### 口径版本：{DEFINITIONS_VERSION}

**1）来料（吨/车）**  
- 来料(吨)、来料(车)直接来自日报记录汇总。

**2）出渣合计（吨）与出渣率**  
- 出渣合计(吨)：优先使用表中“出渣合计(吨)”字段；如缺失，则使用“出渣(吨)”。  
- 出渣率(吨/吨) = 出渣合计(吨) ÷ 来料(吨)

**3）用水量与水耗强度**  
- 用水量(m³)：来自日报“用水量m3”。  
- 水耗强度(m³/吨) = 用水量(m³) ÷ 来料(吨)

**4）用电量与电耗（均摊口径）**  
- 电表读数：来自“电表读数X10^3kWh”，已换算为 kWh。  
- 电表读数通常为不定期抄表点；系统使用相邻两次抄表读数差分得到区间用电量，并**平均分摊到区间内每天**：  
  - 每日电耗(kWh) = (本次抄表读数 - 上次抄表读数) ÷ 区间天数  
  - 均摊区间为 (上次抄表日, 本次抄表日]（不含上一抄表日，含本次抄表日）  
- 用电量(kWh)：为所选时间范围内“每日电耗(kWh)”的求和（属于估算口径）。  
- 电耗强度(kWh/吨) = 用电量(kWh) ÷ 来料(吨)（属于估算口径）。

> 注：若时间范围内抄表点不足（少于 2 个），则无法生成每日电耗/用电量/电耗强度。
"""